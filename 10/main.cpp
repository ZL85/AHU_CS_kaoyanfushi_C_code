//找出1000以内的所有完数，并输出该完数和它的所有因子。
//一个数恰好等于它的因子之和，满足这个条件的数叫完数。、

#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>
int main()
{
    int i, j, sum;
    // 查找1000以内的所有完数
    for (i = 2; i <= 1000; i++)
    {
        sum = 0;
        // 查找i的所有因子
        for (j = 1; j < i; j++)
        {
            if (i % j == 0)
            {
                sum += j;
            }
        }
        // 如果i是完数，则输出它及其因子
        if (sum == i)
        {
            printf("%d是完数，它的因子有：", i);
            for (j = 1; j < i; j++)
            {
                if (i % j == 0)
                {
                    printf("%d ", j);
                }
            }
            printf("\n");
        }
    }
    return 0;
}
//程序的核心部分是两个嵌套的for循环。外层循环从2到1000遍历所有的数，内层循环用来计算每个数的因子和。
//当内层循环结束后，如果因子和等于这个数本身，说明它是完数，程序就会输出它及其所有因子。